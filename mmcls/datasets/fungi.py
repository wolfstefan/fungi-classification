    def __init__(self, *args, ann_file, **kwargs):
        super(FungiTest, self).__init__(*args, ann_file=ann_file, **kwargs)

    def load_annotations(self):
        table = pd.read_csv(self.ann_file)

        def to_dict(img_path, obs_id):
            return {'img_prefix': self.data_prefix, 'img_info': {'filename': img_path, 'observation_id': obs_id}}

        data_infos = [to_dict(img_path, obs_id) for img_path, obs_id in zip(table['filename'], table['ObservationId'])]
        return data_infos
